package model;

public class PersonalDetails {
    private String name;
    private String email;

    // Constructor
    public PersonalDetails(String name, String email) {
        this.name = name;
        this.email = email;
    }

    // Getters and Setters
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getEmail() {
        return email;
    }
}
